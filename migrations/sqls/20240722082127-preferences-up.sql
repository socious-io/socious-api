CREATE TYPE preferences_values AS ENUM (
  'ON',
  'OFF',
  'STRONG_HIGH',
  'MODERATE_HIGH',
  'NEUTRAL',
  'STRONG_LOW',
  'MODERATE_LOW',
  'PREFER_NOT_SAY',
  'DESCRIPTION_ONLY'
);

CREATE TABLE preferences (
  id uuid NOT NULL DEFAULT public.uuid_generate_v4() PRIMARY KEY,
  identity_id uuid NOT NULL,
  title preferences_titles NOT NULL,
  value preferences_values NOT NULL DEFAULT 'DESCRIPTION_ONLY',
  description text DEFAULT NULL,
  UNIQUE(identity_id, title),
  created_at timestamp with time zone NOT NULL DEFAULT now(),
  updated_at timestamp with time zone NOT NULL DEFAULT now(),
  CONSTRAINT fk_identity FOREIGN KEY (identity_id) REFERENCES identities(id) ON DELETE CASCADE
);
